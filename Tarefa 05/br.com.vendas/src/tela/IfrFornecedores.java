    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (tfdNome.getText().length() > 0
                && tfdEmail.getText().length() > 0
                && tffCNPJ.getText().length() > 0
                && tffTelefone.getText().length() > 0) {

            Fornecedores fornecedores = new Fornecedores();
            fornecedores.setId(varID);
            fornecedores.setNome(tfdNome.getText());
            fornecedores.setEmail(tfdEmail.getText());
            fornecedores.setTelefone(tffTelefone.getText());
            fornecedores.setCnpj(tffCNPJ.getText());

            FornecedoresDAO fornecedoresDAO = new FornecedoresDAO();

            if (varID == 0) {
                if (fornecedoresDAO.salvar(fornecedores) == null) {
                    tfdNome.setText("");
                    tfdEmail.setText("");
                    tffTelefone.setText("");
                    tffCNPJ.setText("");

                    JOptionPane.showMessageDialog(this, "Registro salvo com sucesso!");
                    tfdNome.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(this, "Problemas ao salvar registro!");
                }
            } else {
                if (fornecedoresDAO.atualizar(fornecedores) == null) {
                    tfdNome.setText("");
                    tfdEmail.setText("");
                    tffTelefone.setText("");
                    tffCNPJ.setText("");

                    JOptionPane.showMessageDialog(this, "Registro alterado com sucesso!");
                    tfdNome.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(this, "Problemas ao alterar registro!");
                }
            }
            varID = 0;
        } else {
            JOptionPane.showMessageDialog(this, "Favor preencher todos os campos!");
            tfdNome.requestFocus();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int row = tblConsulta.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Selecione um registro na Tabela Consulta!");
        } else {
            String idTabela = String.valueOf(tblConsulta.getValueAt(tblConsulta.getSelectedRow(), 0));
            varID = Integer.parseInt(idTabela);
            Fornecedores fornecedores = new FornecedoresDAO().consultarId(varID);

            if (fornecedores != null) {
                jTabbedPane1.setSelectedIndex(0);
                tfdNome.setText(fornecedores.getNome());
                tfdEmail.setText(fornecedores.getEmail());
                tffCNPJ.setText(fornecedores.getCnpj());
                tffTelefone.setText(fornecedores.getTelefone());

                tfdNome.requestFocus();
            } else {
                JOptionPane.showMessageDialog(this, "CÃ³digo de registro nÃ£o encontrado!");
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

